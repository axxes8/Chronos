package com.team6.chronos;

public class Alert {

    // The variables needed for our alerts to work.
    String name;
    String service;
    String date;
    String time;

    // Alert class constructor.
    public void Alert(String n, String s, String d, String t) {

        name = n;
        service = s;
        date = d;
        time = t;
    }
}
